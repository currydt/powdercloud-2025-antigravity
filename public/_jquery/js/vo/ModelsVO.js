function fullName(v, record){
	var title = "";
	if(record.burial_date != null){
		title += Date.parseDate( record.burial_date, 'c').dateFormat('Y-m-d H:i');
	}
	if( record.surface_snow_form_desc != null && record.surface_snow_form_desc != "" ){
		title += ' / ' + record.surface_snow_form_desc;
		if(record.surface_snow_form_minor_desc != null && record.surface_snow_form_minor_desc != ""){
			title += ' ' + record.surface_snow_form_minor_desc;
		}
	}
	if(record.surface_snow_form_2_desc != null && record.surface_snow_form_2_desc != ""){
		title += ' / ' + record.surface_snow_form_2_desc;
		if(record.surface_snow_form_2_minor_desc != null && record.surface_snow_form_2_minor_desc != ""){
			title += ' ' + record.surface_snow_form_2_minor_desc;
		}
	}
	if(record.nickname_text != null && record.nickname_text != ""){
		title += ' - ' + record.nickname_text;
	}
	return title;
}
